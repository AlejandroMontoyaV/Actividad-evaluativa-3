    private void btnGenenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenenerarActionPerformed
        
        double altura, base,perimetro, area, hipotenusa;
        String tipo;
        
       altura=Double.parseDouble(txtAltura.getText());
       base=Double.parseDouble(txtBase.getText());
       
       TrianguloRectangulo triangulo = new TrianguloRectangulo(altura, base);
       
       hipotenusa=triangulo.hipot();
       tipo= triangulo.clase();
       perimetro=triangulo.perimetro();
       area=triangulo.area();
       
       labelHipot.setText("Hipotenusa: "+hipotenusa);
       labelTipo.setText("Tipo: "+tipo);
       labelPerimetro.setText("Perimetro: "+perimetro);
       labelArea.setText("Area: "+area);                   
    }//GEN-LAST:event_btnGenenerarActionPerformed

